"""
Generation functions to create NLCE Lattices
"""

"""
    generate_coordinates(basis, bravais_lattice, max_order)

Find every coordinate generated by the bravais lattice up till maximum_order,
then repeat for every element of the basis.

```julia-repl
julia> generate_coordinates([[0, 0]], [[0, 1], [1, 0]], 3)
"""
function generate_coordinates(
    basis::Vector{Vector{T}},
    bravais_lattice::Vector{Vector{T}},
    max_order::Int64,
) where {T<:Real}
    dimensions = size(bravais_lattice)[1]
    coordinates::Vector{Vector{T}} = []
    # Cover each basis
    for basis_elem in basis
        # Generate all integers for coordinates in that basis
        for int_coord = 0:((max_order^dimensions)-1)
            coordinate = []
            int_coord_temp = divrem(int_coord, max_order)
            # Find each coordinate 
            for dimension = 1:dimensions
                pushfirst!(coordinate, int_coord_temp[2])
                int_coord_temp = divrem(int_coord_temp[1], max_order)
            end
            # Shift according to the bravais lattice and basis
            push!(coordinates, sum(coordinate .* bravais_lattice) .+ basis_elem)
        end
    end
    coordinates
end

